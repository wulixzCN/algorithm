public class BFSblockNode {
//      int x;
//      int y;
    Integer x;
    Integer y;

    public BFSblockNode() {
    }

    public BFSblockNode(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
